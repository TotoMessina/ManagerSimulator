let idCounter = 10000;
const generarIdUnico = (idEquipo: string): string => {
  return `${idEquipo}-${idCounter++}`;
};

export function randomRange(min: number, max: number): number {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function clamp(value: number, min: number, max: number): number {
  return Math.max(min, Math.min(max, value));
}

export function generarNombreAleatorio(nacionalidad: string): string {
  const pool = NOMBRES_POR_NAC[nacionalidad] || NOMBRES_POR_NAC.Default;
  const nombre = pool[0][Math.floor(Math.random() * pool[0].length)];
  const apellido = pool[1][Math.floor(Math.random() * pool[1].length)];
  return `${nombre} ${apellido}`;
}

