// ============================================================
// 7. GENERADOR AUTOMÁTICO DE FIXTURES DE LIGA (ROUND-ROBIN)
// ============================================================
export function generarFixtureRoundRobin(equiposList: string[], fechaInicio: string): Jornada[] {
  const n = equiposList.length;
  const jornadas: Jornada[] = [];
  const list = [...equiposList];

  if (n % 2 !== 0) {
    list.push('_bye_');
  }
  const numEquipos = list.length;
  let currentDate = new Date(fechaInicio + 'T12:00:00');

  const formatDate = (d: Date): string => {
    return d.toISOString().split('T')[0];
  };

  // Primera vuelta (ida)
  for (let round = 0; round < numEquipos - 1; round++) {
    const partidos: { localId: string; visitanteId: string }[] = [];
    for (let i = 0; i < numEquipos / 2; i++) {
      const local = list[i];
      const visitante = list[numEquipos - 1 - i];
      if (local !== '_bye_' && visitante !== '_bye_') {
        partidos.push({ localId: local, visitanteId: visitante });
      }
    }

    jornadas.push({
      fecha: formatDate(currentDate),
      numero: round + 1,
      partidos
    });

    currentDate.setDate(currentDate.getDate() + 7);
    
    // Rotar lista (mantener el primero, mover el resto)
    const ultimo = list.pop()!;
    list.splice(1, 0, ultimo);
  }

  // Segunda vuelta (vuelta - invertir localía)
  const primeraVueltaCount = jornadas.length;
  for (let round = 0; round < primeraVueltaCount; round++) {
    const partidosInvertidos = jornadas[round].partidos.map(p => ({
      localId: p.visitanteId,
      visitanteId: p.localId
    }));

    jornadas.push({
      fecha: formatDate(currentDate),
      numero: primeraVueltaCount + round + 1,
      partidos: partidosInvertidos
    });

    currentDate.setDate(currentDate.getDate() + 7);
  }

  return jornadas;
}

export const fixtureInicial: Jornada[] = generarFixtureRoundRobin(
  equiposLaLiga.map(e => e.id),
  '2026-08-17'
);
