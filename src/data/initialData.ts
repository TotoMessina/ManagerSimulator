function rellenarPlantel(
  idEquipo: string,
  jugadoresActuales: Jugador[],
  caBase: number,
  nacionalidadesPool: string[],
  sizeMax = 22
): Jugador[] {
  const plantel = [...jugadoresActuales];
  const count = plantel.length;
  if (count >= sizeMax) return plantel;

  const posicionesCount: Record<Posicion, number> = {
    POR: 0, DFC: 0, LD: 0, LI: 0, MC: 0, MCO: 0, ED: 0, EI: 0, DC: 0
  };
  plantel.forEach(j => {
    posicionesCount[j.posicion] = (posicionesCount[j.posicion] || 0) + 1;
  });

  const posicionesMinimas: Record<Posicion, number> = {
    POR: 2, DFC: 3, LD: 2, LI: 2, MC: 3, MCO: 2, ED: 2, EI: 2, DC: 2
  };

  const posicionesFaltantes: Posicion[] = [];
  for (const [pos, min] of Object.entries(posicionesMinimas) as [Posicion, number][]) {
    const faltantes = min - (posicionesCount[pos] || 0);
    for (let i = 0; i < faltantes; i++) {
      posicionesFaltantes.push(pos);
    }
  }

  const POS_POOL: Posicion[] = ['POR', 'DFC', 'LD', 'LI', 'MC', 'MCO', 'ED', 'EI', 'DC'];
  while (posicionesFaltantes.length + count < sizeMax) {
    posicionesFaltantes.push(POS_POOL[posicionesFaltantes.length % POS_POOL.length]);
  }

  const c = sizeMax - count;
  for (let i = 0; i < c; i++) {
    const pos = posicionesFaltantes[i] || POS_POOL[i % POS_POOL.length];
    const nac = nacionalidadesPool[Math.floor(Math.random() * nacionalidadesPool.length)];
    plantel.push(generarJugador(idEquipo, pos, caBase, nac));
  }

  return plantel;
}

function generarTodosLosJugadores(): Jugador[] {
  const personalidades: PersonalidadJugador[] = ['Líder', 'Ambicioso', 'Profesional', 'Problemático', 'Leal'];
  const agrupados: Record<string, Jugador[]> = {};
  cracksIniciales.forEach(j => {
    if (!agrupados[j.idEquipo]) {
      agrupados[j.idEquipo] = [];
    }
    const crackConPers: Jugador = {
      ...j,
      personalidad: j.personalidad || personalidades[Math.floor(Math.random() * personalidades.length)],
      partidosSeguidosBanco: j.partidosSeguidosBanco !== undefined ? j.partidosSeguidosBanco : 0
    };
    agrupados[j.idEquipo].push(crackConPers);
  });

  const todos: Jugador[] = [];
  for (const equipo of equiposIniciales) {
    const config = configEquipos[equipo.id] || { caBase: 65, nacs: ['Default'] };
    const cracks = agrupados[equipo.id] || [];
    const completado = rellenarPlantel(equipo.id, cracks, config.caBase, config.nacs, 26);
    todos.push(...completado);
  }

  // Generar 40 jugadores libres (Agentes Libres)
  const posicionesLibres: Posicion[] = [
    'POR', 'POR', 'POR', 'POR',
    'DFC', 'DFC', 'DFC', 'DFC', 'DFC', 'DFC', 'DFC', 'DFC',
    'LD', 'LD', 'LD', 'LD',
    'LI', 'LI', 'LI', 'LI',
    'MC', 'MC', 'MC', 'MC', 'MC', 'MC', 'MC', 'MC',
    'MCO', 'MCO', 'MCO', 'MCO',
    'ED', 'ED', 'ED',
    'EI', 'EI', 'EI',
    'DC', 'DC', 'DC', 'DC'
  ];
  const nacionalidades = ['España', 'Francia', 'Alemania', 'Italia', 'Argentina', 'Brasil', 'Portugal', 'Inglaterra', 'Holanda', 'Uruguay'];
  
  posicionesLibres.forEach((pos, index) => {
    let caBase = 65;
    if (index < 5) {
      caBase = 80;
    } else if (index < 15) {
      caBase = 75;
    } else if (index < 30) {
      caBase = 65;
    } else {
      caBase = 58;
    }
    const nac = nacionalidades[Math.floor(Math.random() * nacionalidades.length)];
    const jugadorLibre = generarJugador('libre', pos, caBase, nac, 18, 34);
    todos.push(jugadorLibre);
  });

  return todos;
}

